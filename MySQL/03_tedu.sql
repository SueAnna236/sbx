CREATE TABLE emp(
	eid INT PRIMARY KEY AUTO_INCREMENT,
	ename VARCHAR(8) UNIQUE,
	sex BOOL,
	birthday DATE DEFAULT '2001-5-6',
	salary DECIMAL(7,2) DEFAULT 8888.88,
	deptId TINYINT,
	FOREIGN KEY(deptId) REFERENCES dept(did)
);
