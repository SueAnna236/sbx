DROP DATABASE IF EXISTS tedu;
CREATE DATABASE tedu CHARSET=UTF8;
USE tedu;
CREATE TABLE dept(
	did TINYINT PRIMARY KEY,
	dname VARCHAR(16) UNIQUE
);
